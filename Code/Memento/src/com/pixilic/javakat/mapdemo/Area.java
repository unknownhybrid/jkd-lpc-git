package com.pixilic.javakat.mapdemo;

public class Area {
	AreaID id;
	int originX;
	int originY;
	int width;
	int height;
	
	public Area(AreaID id, int originX, int originY, int width, int height){
		this.id = id;
		this.originX = originX;
		this.originY = originY;
		this.width = width;
		this.height = height;
	}
	
}
